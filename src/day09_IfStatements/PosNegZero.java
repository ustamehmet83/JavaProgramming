package day09_IfStatements;

public class PosNegZero {
    public static void main(String[] args) {
        int a = 50;

        if (a < 0) {
            System.out.println(a + " is NEGATIVE");
        } else if (a == 0) {
            System.out.println(a + " is ZERO");

        } else {
            System.out.println(a + " is POSITIVE");
        }

    }


}
