package day07_Operators;

public class UnaryOperators {
    public static void main(String[] args) {

        int a = 200;

        System.out.println(++a); // pre increment: increases value 1 by right away

        int b = 100;

        System.out.println(--b);// pre increment: deccreases value 1 by right away

        int c= 50;

        System.out.println(c++);//print 50 then value be 51  ===> post increment
        System.out.println(c--);//print 51 then value be 50  ===> post decrement
        System.out.println(c); //print 50 then value be 50




    }



}
